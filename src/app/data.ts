const seededRandom = (seed: number) => {
    let s = seed;
    return () => {
        s = (s * 16807 + 0) % 2147483647;
        return s / 2147483647;
    };
};

const generatePriceChart = (base: number, trendFactor: number, volatility: number, seed: number): ChartPoint[] => {
    const rng = seededRandom(seed);
    const start = new Date("2026-02-02T00:00:00Z");
    let price = base;
    return Array.from({ length: 30 }).map((_, i) => {
        const trend = 1 + trendFactor / 30;
        const noise = 1 + (rng() - 0.5) * 2 * volatility;
        price = price * trend * noise;
        return {
            time: new Date(start.getTime() + i * 24 * 60 * 60 * 1000).toLocaleDateString("en-US", {
                month: "short",
                day: "numeric",
                timeZone: "UTC",
            }),
            value: Math.round(price * 100) / 100,
        };
    });
};

const generateVolumeChart = (base: number, volatility: number, seed: number): ChartPoint[] => {
    const rng = seededRandom(seed);
    const start = new Date("2026-02-02T00:00:00Z");
    return Array.from({ length: 30 }).map((_, i) => {
        const noise = 1 + (rng() - 0.5) * 2 * volatility;
        return {
            time: new Date(start.getTime() + i * 24 * 60 * 60 * 1000).toLocaleDateString("en-US", {
                month: "short",
                day: "numeric",
                timeZone: "UTC",
            }),
            value: Math.round(base * noise),
        };
    });
};

